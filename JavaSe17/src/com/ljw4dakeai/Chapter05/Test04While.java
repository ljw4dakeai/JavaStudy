package com.ljw4dakeai.Chapter05;

/**
 * @author ZJH
 * @info while  循环的使用
 */
public class Test04While {
    public static void main(String[] args) {

        //for 和 while 除了写法不一样 其他都是一样的！
        int i = 0;
        while (i < 100) {
            System.out.println("Hello Word!");
            i++;
        }
    }
}
